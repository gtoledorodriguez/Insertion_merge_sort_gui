package sorting; 
/**
 * This interface keeps track of the size of the unsorted/array and the values of the unsorted/sorted array
 * @author grace
 *
 */
public interface Values {
	// Global variable SIZE is the size of the array to be sorted
	int SIZE = 100;
	// Global variable values is the array of values to be sorted
	int[] values = new int[SIZE];
}